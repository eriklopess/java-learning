public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");
        
        /*
        *
        * Tipos Primitivos em Java
        *   byte - 8 bits - -128 a 127
        *   short - 16 bits - -32768 a 32767
        *   int - 32 bits - -2147483648 a 2147483647
        *   long - 64 bits - Muitos numeros a Vários numeros
        *   float - 32 bits - Numeros demais com ponto
        *   double - 64 bits - Mais numeros aindas com ponto
        *   char - 16 bits - 1 Caracter qualquer
        *   boolean - 1 bit - true, false
        *   String - Cadeia de cacteres (Palavras ou texto)
        *
        */

        byte EXEMPLO_BYTE = 127;
        System.out.println(EXEMPLO_BYTE);

        short EXEMPLO_SHORT = 30000;
        System.out.println(EXEMPLO_SHORT);

        int EXEMPLO_INT = 10000000;
        System.out.println(EXEMPLO_INT);

        long EXEMPLO_LONG = 1000000000;
        System.out.println(EXEMPLO_LONG);

        double EXEMPLO_DOUBLE = 1.23;
        System.out.println(EXEMPLO_DOUBLE);

        float EXEMPLO_FLOAT = 8542.658784F;
        System.out.println(EXEMPLO_FLOAT);

        char EXEMPLO_CHAR = 'E';
        System.out.println(EXEMPLO_CHAR);

        boolean EXEMPLO_BOOLEAN = true;
        System.out.println(EXEMPLO_BOOLEAN);

        /*
        * Observações
        * Nunca começar uma variavel com digito de numeros
        */
    }
}
